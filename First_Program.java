public class First_Program {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}

// 1. Functions = A block of code that performs a specific task and is contained within a class or interface
// 2. Classes   = A class is a blueprint for creating objects. In Java, every program must be contained within a class.


/* package = Used to group related class. Think of it as a folder in a file directory
 * public  = Access modifier : Specifies that the class or method is accessible from anywhere in your program.
 * static  = Keyword         : Makes it possible to run the program without calling the function
 * void    = Keyword         : It means that it doesn't give anything in return
 * main    = Method name     : Name of function
 * system  = Class           : A built-in Java class that provides access to system resources.
 * out     = Object          : A static member of the System class. Represents the standard output stream (usually the console).
 * println = Method          : Prints a line of text to the output and moves the cursor to a new line.
 */

